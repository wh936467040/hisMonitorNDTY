package model;

public class DbBufBean {
	private float buffWaitRate;
	private float buffReadRate;
	private float buffWriteRate;
	public float getBuffWaitRate() {
		return buffWaitRate;
	}
	public void setBuffWaitRate(float buffWaitRate) {
		this.buffWaitRate = buffWaitRate;
	}
	public float getBuffReadRate() {
		return buffReadRate;
	}
	public void setBuffReadRate(float buffReadRate) {
		this.buffReadRate = buffReadRate;
	}
	public float getBuffWriteRate() {
		return buffWriteRate;
	}
	public void setBuffWriteRate(float buffWriteRate) {
		this.buffWriteRate = buffWriteRate;
	}
	
	
	

}
